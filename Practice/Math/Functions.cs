
namespace Practice.Math
{
    static class Functions
    {
        public static int Add(int a, int b)
        {
            return a + b;
        }

        public static int Multiply(int a, int b)
        {
            return a * b;
        }

        public static double Add(double a, double b)
        {
            return a + b;
        }
    }
}
